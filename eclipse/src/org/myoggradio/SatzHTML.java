package org.myoggradio;

public class SatzHTML 
{
	private String html = "";
	private String charset = "";
	public void setHTML(String s)
	{
		html = s;
	}
	public void setCharset(String s)
	{
		charset = s;
	}
	public String getHTML()
	{
		return html;
	}
	public String getCharset()
	{
		return charset;
	}
}
